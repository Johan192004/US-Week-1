    private void addProductBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addProductBtnActionPerformed
        
        String nameProduct = JOptionPane.showInputDialog(rootPane, "Ingrese el nombre del producto", "Ventana", JOptionPane.QUESTION_MESSAGE); 
        if(nameProduct == null) return;
        
        if(productNames.contains(nameProduct)){
            JOptionPane.showMessageDialog(rootPane, "El producto con nombre " + nameProduct + "ya esta registrado.", "Error", HEIGHT);
        }

        Double price = this.readDouble("Ingrese el precio del producto", "Ventana", JOptionPane.QUESTION_MESSAGE);            
        if(price == null) return;

        Integer stock = this.readInteger("Ingrese el stock del producto", "Ventana", JOptionPane.QUESTION_MESSAGE);
        if(stock == null) return;
        
        addProduct(nameProduct,price,stock);
        
    }//GEN-LAST:event_addProductBtnActionPerformed

    
    // Utilitary methods
    public void addProduct(String nameProduct,double price, int stock){
        
        productNames.add(nameProduct);
        prices[productNames.size()-1] = price;
        stockProducts.put(nameProduct, stock);
        
        expandPrices();
        JOptionPane.showMessageDialog(rootPane, "Se ha agregado el producto exitosamente.", "Exito", JOptionPane.PLAIN_MESSAGE);
        
    }
    
    public void expandPrices(){
        
        int currentLen = prices.length;
        double[] copyPrices = prices.clone();
        prices = new double[currentLen + 1];
        
        for(int i = 0; i<prices.length; i++){
            prices[i] = copyPrices[i];
        }
        
    }
    
    public int indexOfName(String nameProduct){
        
        return productNames.indexOf(nameProduct);
        
    }
    
    public Integer readInteger(String message, String title, int typeOfMessage){
        boolean intPass = false;
        int number = 0;

        do{
            try{
                String intMessage = JOptionPane.showInputDialog(rootPane,message ,title, typeOfMessage);

                if(intMessage == null){
                    return null;
                }
                number = Integer.parseInt(intMessage);


                if(number < 0){
                    JOptionPane.showMessageDialog(rootPane,"Solo puedes ingresar numeros positivos");
                } else {
                    intPass = true;
                }

            } catch (NumberFormatException e){
                JOptionPane.showMessageDialog(rootPane, "Haz ingresado un numero no valido", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } while (!intPass);

        return number;
    }
    
    public Double readDouble(String message, String title, int typeOfMessage){
        boolean doublePass = false;
        double doubleNumber = 0;

        do{
            try{
                String doubleMessage = JOptionPane.showInputDialog(rootPane,message ,title, typeOfMessage);

                if(doubleMessage == null){
                    return null;
                }
                doubleNumber = Double.parseDouble(doubleMessage);


                if(doubleNumber < 0){
                    JOptionPane.showMessageDialog(rootPane,"Solo puedes ingresar numeros positivos decimales.");
                } else {
                    doublePass = true;
                }

            } catch (NumberFormatException e){
                JOptionPane.showMessageDialog(rootPane, "Haz ingresado un numero decimal no valido", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } while (!doublePass);

        return doubleNumber;
    }
    
